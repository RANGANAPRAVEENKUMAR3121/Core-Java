
public class WhileLoop {

	public static void main(String[] args) {
			// when we don't know no.of iteration while loop will be used
		
			int i = 1;
			while(i<=10) {
				System.out.println(i);
				i++;
			}
			
			while(true) {
				System.out.println("Hello");
				break;
			}
		
	}

}
